/**
 * op_add - Compute the sum of two int
 * @a: first int
 * @b: Second int
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Compute the difference of two int
 * @a: first int
 * @b: Second int
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Compute the product of two int
 * @a: first int
 * @b: Second int
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Compute the div of two int
 * @a: first int
 * @b: Second int
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Compute the modulo of two int
 * @a: first int
 * @b: Second int
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
